for k in range(3, 20):
    str_ = ''
    for i in (1, k - 1):
        for j in (i + 1, k - 1):
            if k % (i + j) == 0:
                str_ = (f'Если число в открытой вставке = {k}, ключи к открытию ворот пары чисел {i, j}')
    print(str_)

